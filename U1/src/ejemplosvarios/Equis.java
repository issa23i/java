/**
 * Realiza un programa que pinte una X hecha de asteriscos.
 * El programa debe pedir la altura.
 * Se debe comprobar que la altura sea un número impar mayor
 * o igual a 3, en caso contrario se debe mostrar
 * un mensaje de error.
 */

import java.util.Scanner;



public class Equis {
    public static void main(String args[]) {
        int altura;
        do {
            // pedir altura
            Scanner teclado = new Scanner(System.in);
            System.out.println("Escriba un número impar, mayor o igual que 3: ");
            altura = teclado.nextInt();
            // validar altura
        } while ( !( (altura >= 3) && (altura % 2 != 0) ) );

        // Dibujar Equis

        // Primera línea
        for( int i = 0; i <= altura; i++) {
            for ( int j = 0; j <= altura; j++) {
                if ( j == i ) {
                    System.out.println("@");
                } else {
                    System.out.println(" ");
                }
            }
        }

    }
}
